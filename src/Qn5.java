public class Qn5 {
    //5. Write a Java program to swap two variables.
    public Qn5() {
        int x = 10;
        int y= 20;

        System.out.println("value of x is :"+ x );
        System.out.println("value of y is :"+ y );

        int temp = x;
        x = y;
        y=temp;

        System.out.println("value of x is :"+ x );
        System.out.println("value of y is :"+ y );

    }
}
